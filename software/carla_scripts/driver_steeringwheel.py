class World(object):
    def __init__(self, carla_world, hud, args, scenario, client, path_details):
        self.world = carla_world
        self.sync = args.sync
        self.path_details = path_details
        self.scenario = scenario
        self.client = client
        self.scenario_name = args.scenario
        self.participant_name = args.name
        
        self.spawn_point = None
        self.init_spawn_point(path_details["start_location"])
        self.hud = hud
        self.player = None
        self.collision_sensor = None
        self.lane_invasion_sensor = None
        self.gnss_sensor = None
        self.camera_manager = None
        self._weather_presets = find_weather_presets()
        self._weather_index = 0
        self._actor_filter = args.filter
        self._gamma = args.gamma
        self.restart(args)
        
        # All about the simulation
        self.simulation_clock = None
        self.simulation_start_tick = 0
        self.simulation_time = 0
        self.manager = None
        
        self.world.on_tick(hud.on_world_tick)

    def init_spawn_point(self, start_location): 
        try:
            self.spawn_point = carla.Transform(carla.Location(x=start_location["x"], y=start_location["y"], z=start_location["z"]), carla.Rotation(pitch=0.0, yaw=start_location["yaw"], roll=0.00000))
        except Exception as e:
            logger.error("Could not initiate spawn location for the driver", e)

    def restart(self, args):
        # Keep same camera config if the camera manager exists.
        cam_index = self.camera_manager.index if self.camera_manager is not None else 0
        cam_pos_index = self.camera_manager.transform_index if self.camera_manager is not None else 0
        # Get a random blueprint.
        blueprint = random.choice(self.world.get_blueprint_library().filter(self._actor_filter))
        blueprint.set_attribute('role_name', 'hero')
        if blueprint.has_attribute('color'):
            blueprint.set_attribute('color', '{},{},{}'.format(args.red, args.green, args.blue))
        # Spawn the player.
        if self.player is not None:
            spawn_point = self.player.get_transform()
            spawn_point.location.z += 2.0
            spawn_point.rotation.roll = 0.0
            spawn_point.rotation.pitch = 0.0
            self.destroy()
            self.player = self.world.try_spawn_actor(blueprint, spawn_point)
        while self.player is None:
            self.player = self.world.try_spawn_actor(blueprint, self.spawn_point)
        # Set up the sensors.
        self.collision_sensor = CollisionSensor(self.player, self.hud)
        self.lane_invasion_sensor = LaneInvasionSensor(self.player, self.hud)
        self.gnss_sensor = GnssSensor(self.player)
        self.camera_manager = CameraManager(self.player, self.hud, args.gamma)
        self.camera_manager.transform_index = cam_pos_index
        self.camera_manager.set_sensor(cam_index, notify=False)
        actor_type = get_actor_display_name(self.player)
        self.hud.notification(actor_type)
        
        if self.sync:
            self.world.tick()
        else:
            self.world.wait_for_tick()

    def next_weather(self, reverse=False):
        self._weather_index += -1 if reverse else 1
        self._weather_index %= len(self._weather_presets)
        preset = self._weather_presets[self._weather_index]
        self.hud.notification('Weather: %s' % preset[1])
        self.player.get_world().set_weather(preset[0])

    def startScenario(self):
        if self.simulation_clock is None:
            logger.info("Starting the scenario")
            self.scenario.start()
            self.hud.notification('The simulation has started')
            self.manager = Manager(self.path_details["path_checkpoints"], self.world, self.player, self.participant_name, self.scenario_name)
            #self.collision_sensor = self.manager.collision_sensor
            self.simulation_clock = pygame.time.Clock()
            self.simulation_start_tick = pygame.time.get_ticks()
            
    def finishScenario(self):
        logger.warning("Finish was NOT REACHED. Terminating the simulation.")
        self.manager.shut_down(False)
        self.scenario.finish(self.client)

    def tick(self):
        if self.simulation_clock is not None:
            self.simulation_clock.tick(60)
            self.simulation_time += self.simulation_clock.get_time()
            if self.manager.record(self.simulation_time):
                return True
            self.hud.tick(self, self.simulation_time/1000)
        else:
            self.hud.tick(self, 0)

    def render(self, display):
        self.camera_manager.render(display)
        self.hud.render(display)

    def destroy(self):
        sensors = [
            self.camera_manager.sensor,
            self.collision_sensor.sensor,
            self.lane_invasion_sensor.sensor,
            self.gnss_sensor.sensor]
        for sensor in sensors:
            if sensor is not None:
                sensor.stop()
                sensor.destroy()
        if self.player is not None:
            self.player.destroy()

