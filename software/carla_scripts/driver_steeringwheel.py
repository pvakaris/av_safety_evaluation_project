def game_loop(args, scenario, path_details):
    pygame.init()
    pygame.font.init()
    world = None
    original_settings = None
    try:
        client = carla.Client(args.host, args.port)
        client.set_timeout(20.0)
        sim_world = client.get_world()
        if args.sync:
            original_settings = sim_world.get_settings()
            settings = sim_world.get_settings()
            if not settings.synchronous_mode:
                settings.synchronous_mode = True
                settings.fixed_delta_seconds = 0.05
            sim_world.apply_settings(settings)
            traffic_manager = client.get_trafficmanager()
            traffic_manager.set_synchronous_mode(True)

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)
        
        display.fill((0,0,0))
        pygame.display.flip()
        ### MY PART ###
        scenario.setup(traffic_manager, sim_world)
        scenario.spawn(client)
        logger.info("The scenario was set. Spawning the hero vehicle and launching the simulation")
        ###   ###   ###
        hud = HUD(args.width, args.height)
        try:
            world = World(sim_world, hud, args, scenario, client, path_details)
            controller = DualControl(world, args.autopilot)
        except Exeption as e:
            logger.error(e)
        if args.sync:
            sim_world.tick()
        else:
            sim_world.wait_for_tick()

        clock = pygame.time.Clock()
        while True:
            try:
                if args.sync:
                    sim_world.tick()
                clock.tick_busy_loop(FPS)
                if controller.parse_events(client, world, clock, args.sync):
                    return
                if world.tick():
                    return
                world.render(display)
                pygame.display.flip()
            except Exception as e:
                logger.error(e)
    except Exception as e:
        logger.error("Something went wrong trying to launch the driver.py", e)
    finally:
        ### MY PART ###
        if(scenario.populated):
            scenario.finish(client)
        if(world and world.manager):
            logger.info('Total time simulated: {}s'.format(str(world.simulation_time/1000)))
        ###   ###   ###

        if original_settings:
            sim_world.apply_settings(original_settings)

        if world is not None:
            world.destroy()

        pygame.quit()


